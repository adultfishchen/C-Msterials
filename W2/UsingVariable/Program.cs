using System;

namespace UsingVariable
{
    class Program
    {
        static void Main(string[] args)
        {
            char aChar = 'a';
            Console.WriteLine("aChar = "+ aChar);
            int anInt = 123;
            Console.WriteLine("anInt = "+ anInt);
            double aDouble;
            aDouble = 123.456;
            Console.WriteLine("aDouble = "+ aDouble);
            bool aBool = true;
            Console.WriteLine("aBool = "+ aBool);

            Console.ReadKey();
        }
    }
}
